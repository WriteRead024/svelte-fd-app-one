<p>
    The SvelteKit configuration file allows setting CSP Content Security Policy for the app.&nbsp;
    More information is available at the CSP Mozilla Developer Network page on 
    <a class="ext" href="https://developer.mozilla.org/en-US/docs/Web/HTTP/Headers/Content-Security-Policy">Content Security Policy</a>.&nbsp; 
    The SvelteKit documentation specifically mentions the 
    <a class="ext" href="https://developer.mozilla.org/en-US/docs/Web/HTTP/Headers/Content-Security-Policy-Report-Only">Report Only mode of CSP</a>
    which can be used to report when a page is loaded.
