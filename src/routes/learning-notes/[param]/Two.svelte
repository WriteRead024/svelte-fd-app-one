<p>
    <a href="https://kit.svelte.dev/docs/routing">routing</a>:
    The router is based on the file system folder structure.&nbsp;
    It requires that files be specially named:
    '+page.svelte', '+page.js', '+page.server.js', 'error.svelte', '+layout.svelte', '+layout.js', '+layout.server.js'.&nbsp;
    A 'server.js' file can be used to define server-side routes for the normal HTTP verbs.&nbsp;
    Typescript types can be defined in some or all of those files
    <a href="https://svelte.dev/blog/zero-config-type-safety">(more info on types)</a>.&nbsp;
</p>
